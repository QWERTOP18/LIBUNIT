#include "ft_string.h"
#include <stddef.h>

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	len;

	len = ft_strlen(src);
	if (!dstsize)
		return (len);
	while (1 < dstsize-- && *src)
		*(dst++) = *(src++);
	*dst = '\0';
	return (len);
}
