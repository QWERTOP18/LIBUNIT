#include "ft_string.h"
#include <stddef.h>
#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
	char	*s2;
	size_t	size;

	size = ft_strlen(s1) + 1;
	s2 = (char *)malloc(size * sizeof(char));
	if (s2)
		ft_strlcpy(s2, s1, size);
	return (s2);
}
