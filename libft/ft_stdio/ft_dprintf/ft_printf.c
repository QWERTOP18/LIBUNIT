#include "ft_printf.h"

int	ft_printf(const char *format, ...)
{
	va_list	ap;
	int		cnt;

	va_start(ap, format);
	cnt = dprintf_format_string(STDOUT_FILENO, format, ap);
	va_end(ap);
	return (cnt);
}
